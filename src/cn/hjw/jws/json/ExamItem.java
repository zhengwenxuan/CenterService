package cn.hjw.jws.json;

public class ExamItem {
	private String examItemType = "";
	private String examItemId = "";
	private String examItemName = "";
	private String examItemValue = "";
	private String itemnit = "";
	private String ref = "";
	private String flag = "";
	private String isDetail = "";
	private String lineChart="0";
	private String isCompare="1";
	public String getLineChart() {
		return lineChart;
	}

	public void setLineChart(String lineChart) {
		this.lineChart = lineChart;
	}

	public String getExamItemType() {
		return examItemType;
	}

	public void setExamItemType(String examItemType) {
		this.examItemType = examItemType;
	}

	public String getExamItemId() {
		return examItemId;
	}

	public void setExamItemId(String examItemId) {
		this.examItemId = examItemId;
	}

	public String getExamItemName() {
		return examItemName;
	}

	public void setExamItemName(String examItemName) {
		this.examItemName = examItemName;
	}

	public String getExamItemValue() {
		return examItemValue;
	}

	public void setExamItemValue(String examItemValue) {
		this.examItemValue = examItemValue;
	}

	public String getItemnit() {
		return itemnit;
	}

	public void setItemnit(String itemnit) {
		this.itemnit = itemnit;
	}

	public String getRef() {
		return ref;
	}

	public void setRef(String ref) {
		this.ref = ref;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public String getIsDetail() {
		return isDetail;
	}

	public void setIsDetail(String isDetail) {
		this.isDetail = isDetail;
	}

	public String getIsCompare() {
		return isCompare;
	}

	public void setIsCompare(String isCompare) {
		this.isCompare = isCompare;
	}

}
